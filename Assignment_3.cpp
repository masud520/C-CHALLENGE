#include <iostream>
using namespace std;

int main() {
    // Stick figure woman and man holding hands
    cout << "     O     O         " << endl;  // Heads
    cout << "    /|\\  /|\\       " << endl;  // Arms and bodies
    cout << "    / \\  / \\       " << endl;  // Legs
    cout << "     |____|          " << endl;  // Holding hands
    cout << "    / \\  / \\       " << endl;  // Legs and feet
    cout << "    \\_/  \\_/        " << endl;  // Feet

    // Empty line for separation
    cout << "                    " << endl;

    // Car ASCII art
    cout << "       ____         " << endl;  // Car roof
    cout << "  ____/    \\____    " << endl;  // Car body top
    cout << " /     ___     \\    " << endl;  // Car body middle
    cout << "/_____/   \\_____\\   " << endl;  // Car body bottom
    cout << "|     |___|     |   " << endl;  // Car windows
    cout << "|_____|   |_____|   " << endl;  // Car doors
    cout << " | |         | |    " << endl;  // Car wheels
    cout << " | |         | |    " << endl;  // Car wheels

    // Empty line for separation
    cout << "                   " << endl;

    // Ground line
    cout << "____________________" << endl;  // Ground

    return 0;
}
